#include "libft.h"

void	printbit(int nb)
{
	unsigned int	mask;

	mask = (1 << 31);
	while (mask)
	{
		if (nb & mask)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		mask >>= 1;
	}
	write(1, "\n", 1);
}
